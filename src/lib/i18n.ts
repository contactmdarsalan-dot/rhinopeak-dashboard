const runtimePatterns: Array<[RegExp, string]> = [
  [/^Welcome back, (.+)\.$/, 'फेरि स्वागत छ, $1।'],
  [/^Welcome, (.+)\.$/, 'स्वागत छ, $1।'],
  [/^Connected to backend API\.$/, 'ब्याकएन्ड API सँग जोडियो।'],
  [/^Connected to MongoDB API at (.+)\.$/, 'MongoDB API सँग जोडियो: $1।'],
  [/^You have been logged out\.$/, 'तपाईं लग आउट हुनुभयो।'],
];

const translatedEnglishValues = Object.keys(translations.en).reduce<Record<string, string>>((lookup, key) => {
  const typedKey = key as TranslationKey;
  lookup[translations.en[typedKey]] = translations.ne[typedKey];
  return lookup;
}, {});

export function translate(language: AppLanguage | undefined, key: TranslationKey) {
  return translations[language ?? 'en']?.[key] ?? translations.en[key];
}

export function languageName(language: AppLanguage) {
  return language === 'ne' ? 'नेपाली' : 'English';
}

export function uiText(language: AppLanguage | undefined, text: string) {
  if ((language ?? 'en') !== 'ne') return text;
  const direct = uiTextNepali[text];
  if (direct) return direct;
  const translatedValue = translatedEnglishValues[text];
  if (translatedValue) return translatedValue;
  if (/^\d+ entries$/.test(text)) return text.replace('entries', 'प्रविष्टि');
  if (/^\d+ files$/.test(text)) return text.replace('files', 'फाइल');
  for (const [pattern, replacement] of runtimePatterns) {
    if (pattern.test(text)) return text.replace(pattern, replacement);
  }
  return text;
}

export function uiFormat(language: AppLanguage | undefined, template: string, values: Record<string, string | number>) {
  return Object.entries(values).reduce(
    (message, [key, value]) => message.replaceAll(`{${key}}`, String(value)),
    uiText(language, template)
  );
}

